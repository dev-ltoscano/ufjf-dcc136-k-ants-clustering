#!/bin/bash

PROGRAM="KAntsClustering"

INSTANCE_FOLDER="Instances"
INSTANCE_IRIS="${INSTANCE_FOLDER}/iris.txt"
INSTANCE_SPIRAL="${INSTANCE_FOLDER}/spiral.txt"
INSTANCE_R15="${INSTANCE_FOLDER}/R15.txt"
INSTANCE_DIM032="${INSTANCE_FOLDER}/dim032.txt"
INSTANCE_DIM128="${INSTANCE_FOLDER}/dim128.txt"
INSTANCE_S1="${INSTANCE_FOLDER}/s1.txt"
INSTANCE_S2="${INSTANCE_FOLDER}/s2.txt"
INSTANCE_S3="${INSTANCE_FOLDER}/s3.txt"
INSTANCE_S4="${INSTANCE_FOLDER}/s4.txt"

OUTPUT_FOLDER="Result-Basic"
OUTPUT_FOLDER_IRIS="${OUTPUT_FOLDER}/iris/"
OUTPUT_FOLDER_SPIRAL="${OUTPUT_FOLDER}/spiral/"
OUTPUT_FOLDER_DIM032="${OUTPUT_FOLDER}/dim032/"
OUTPUT_FOLDER_DIM128="${OUTPUT_FOLDER}/dim128/"
OUTPUT_FOLDER_R15="${OUTPUT_FOLDER}/R15/"
OUTPUT_FOLDER_S1="${OUTPUT_FOLDER}/s1/"
OUTPUT_FOLDER_S2="${OUTPUT_FOLDER}/s2/"
OUTPUT_FOLDER_S3="${OUTPUT_FOLDER}/s3/"
OUTPUT_FOLDER_S4="${OUTPUT_FOLDER}/s4/"

rm -rf ${OUTPUT_FOLDER}
mkdir -p ${OUTPUT_FOLDER_IRIS} ${OUTPUT_FOLDER_SPIRAL} ${OUTPUT_FOLDER_R15} ${OUTPUT_FOLDER_DIM032} ${OUTPUT_FOLDER_DIM128} ${OUTPUT_FOLDER_S1} ${OUTPUT_FOLDER_S2} ${OUTPUT_FOLDER_S3} ${OUTPUT_FOLDER_S4}

echo "Running: ${INSTANCE_IRIS}"
./${PROGRAM} -t ${INSTANCE_IRIS} 150 2 3 2 5000 ${OUTPUT_FOLDER_IRIS} 0.1 0.15 0.15 -v >> "${OUTPUT_FOLDER_IRIS}console.out"

echo "Running: ${INSTANCE_SPIRAL}"
./${PROGRAM} -t ${INSTANCE_SPIRAL} 312 3 3 2 5000 ${OUTPUT_FOLDER_SPIRAL} 0.1 0.15 0.15 -v >> "${OUTPUT_FOLDER_SPIRAL}console.out"

echo "Running: ${INSTANCE_R15}"
./${PROGRAM} -t ${INSTANCE_R15} 600 3 15 4 5000 ${OUTPUT_FOLDER_R15} 0.1 0.15 0.15 -v >> "${OUTPUT_FOLDER_R15}console.out"

echo "Running: ${INSTANCE_DIM032}"
./${PROGRAM} -t ${INSTANCE_DIM032} 1024 32 16 6 5000 ${OUTPUT_FOLDER_DIM032} 0.1 0.15 0.15 -v >> "${OUTPUT_FOLDER_DIM032}console.out"

echo "Running: ${INSTANCE_DIM128}"
./${PROGRAM} -t ${INSTANCE_DIM128} 1024 128 16 6 5000 ${OUTPUT_FOLDER_DIM128} 0.1 0.15 0.15 -v >> "${OUTPUT_FOLDER_DIM128}console.out"

echo "Running: ${INSTANCE_S1}"
./${PROGRAM} -t ${INSTANCE_S1} 5000 2 15 8 5000 ${OUTPUT_FOLDER_S1} 0.1 0.15 0.15 -v >> "${OUTPUT_FOLDER_S1}console.out"

echo "Running: ${INSTANCE_S2}"
./${PROGRAM} -t ${INSTANCE_S2} 5000 2 15 8 5000 ${OUTPUT_FOLDER_S2} 0.1 0.15 0.15 -v >> "${OUTPUT_FOLDER_S2}console.out"

echo "Running: ${INSTANCE_S3}"
./${PROGRAM} -t ${INSTANCE_S3} 5000 2 15 8 5000 ${OUTPUT_FOLDER_S3} 0.1 0.15 0.15 -v >> "${OUTPUT_FOLDER_S3}console.out"

echo "Running: ${INSTANCE_S4}"
./${PROGRAM} -t ${INSTANCE_S4} 5000 2 15 8 5000 ${OUTPUT_FOLDER_S4} 0.1 0.15 0.15 -v >> "${OUTPUT_FOLDER_S4}console.out"
